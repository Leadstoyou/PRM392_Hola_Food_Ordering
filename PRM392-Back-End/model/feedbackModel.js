import mongoose, { Schema } from "mongoose";

const Feedback = mongoose.model(
  "Feedback",
  new Schema({
    user: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    product: {
      type: Schema.Types.ObjectId,
      ref: "Product",
      required: true,
    },
    text: String,
  })
);
export default Feedback;
